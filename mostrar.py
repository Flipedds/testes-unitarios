def somar(x, y):
    return x + y


def mostrar(nome):
    return nome


def hello_word(hello):
    return f'hello word, {hello}'


def split(nome):
    separar = nome.split()
    return separar


def maior_menor(x, y):
    return 'x maior' if x > y else 'y maior'


def recebe(um, dois):
    return somar(um, dois)


